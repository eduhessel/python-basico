#Não tenho casas decimais int
print('Tipo int:', 1, '=>', type(1))
#Tenho casas decimais float
print('Tipo float:', 1.0, '=>', type(1.0))
#Texto
print('Tipo string:', 'string', '=>', type('string'))
#Booleano se é verdadeiro ou falso
print('Tipo bool:', True, '=>', type(True))
print('Tipo bool:', False, '=>', type(False))
#Lista
print('Tipo list:', [1,2,3], '=>', type([1,2,3]))